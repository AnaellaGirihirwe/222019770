//A program to print 20 numbers but stops when The number is equal to 10
public class printnum {  // Declares a public class called printnum
        public static void main(String[] args) { // Shows the main method  
            for(int i=1;i<=20;i++){  //Declares the for loop to iterate from 1 to 20
                if(i==10){ //Checks if the number equals to 10  
                    break; //If it is,break out the loop
                } //ends if statement 
                System.out.println(i);  // Prints the numbers
            } // ends the for loop
        }  // ends the main method
        } // ends the printnum class
    


