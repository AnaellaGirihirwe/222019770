//A program to print the first 50 positive numbers
public class numbers {// declares the public class numbers
    public static void main(String[] args) {// shows the main method
        for (int i = 2; i <= 50; i += 2) {// declares the for loop to iterate from 2 to 50
            System.out.println(i);//prints the number
        }//ends for loop
    }//ends main method
}//ends public class numbers