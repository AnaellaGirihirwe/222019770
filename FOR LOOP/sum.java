//A program to print the sum of all numbers from1 to 100
public class sum {// declares the public class sum
    public static void main(String[] args) {// shows the main method
        int sum=0;// declares the variable sum and initializes it to 0
        for (int i = 1; i <= 100; i += 1) {// declares for loop to iterate from 1 to 100
            sum=sum+i;//increments sum by i
            System.out.println(sum);// prints the sum of numbers
        }// ends for loop
    }//ends the main method
}//ends the public class sum
    

