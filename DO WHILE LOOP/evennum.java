//A program to print  all even numbers from 0 to 100
public class evennum{//Declares the public class evennum
        public static void main(String[] args) {// shows the main method
            int n = 0;//Declares the variable n and initializes it to 0
            do {// starts the do while loop
                System.out.println(n);//prints the value of n
                n += 2;// Increments n by 2
            } while (n <= 100);// shows the condition for the loop to execute
        }//ends main method
    }// ends the public class evennum
    


