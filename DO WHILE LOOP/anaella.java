//A program to print Anaella 5 times
public class anaella {// Declares a public class called anaella
    public static void main(String[] args) {// shows the main method
        int i = 0;// Declares a variable i and initializes it to 0
        do {// starts a do-while loop
            System.out.println("anaella");// prints the name anaella
            i++;// Increments variable i by 1
        } while (i < 5);// the condition for the loop to execute
    } //ends the main method
}//ends the anaella class